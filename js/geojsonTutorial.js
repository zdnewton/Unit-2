//creates map with initial view and zoom threshhold
var map = L.map('map').setView([39.75621,-104.9940], 4);
//loads a tile layer and sets the maxium zoom level, adds source information
L.tileLayer('http://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
    attribution: '&copy; <a href="http://www.openstreetmap.org/copyright">OpenStreetMap contributors</a>'
}).addTo(map);
//creates a geojson feature and establishes both attributes and geometry
var geojsonFeature = {
    "type": "Feature",
    "properties": {
        "name": "Coors Field",
        "amenity": "Baseball Stadium",
        "popupContent": "This is where the Rockies play!"
    },
    "geometry": {
        "type": "Point",
        "coordinates": [-104.99404, 39.75621]
    }
};
//adds the feature to the map
L.geoJSON(geojsonFeature,{onEachFeature: onEachFeature}).addTo(map);
//creates an array of line objects 
